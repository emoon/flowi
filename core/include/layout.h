#pragma once

#include <stdint.h>
#include "../include/string.h"

struct FlContext* ctx;

typedef uint32_t FlLayoutId;
typedef uint32_t FlLayoutAreaId;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

typedef struct FlLayoutRect {
    float x, y, width, height;
} FlLayoutRect;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

typedef enum FlLayoutDirection {
    FlLayoutDirection_Horizontal,
    FlLayoutDirection_Directional,
} FlLayoutDirection;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

typedef enum FlSizeType {
    FlSizeType_Fixed,
    FlSizeType_Stretch,
} FlSizeType;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

typedef struct FlSizing {
    int value;
    FlSizeType type;
} FlSizing;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

typedef struct FlLayoutArea {
    FlString name;
    FlSizing width;
    FlSizing height;
    FlLayoutRect rect;
    FlLayoutRect reserving_rect;
} FlLayoutArea;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

typedef struct FlLayout {
    FlLayoutRect size;
} FlLayout;

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

FlLayoutId fl_layout_create(struct FlContext* ctx, struct FlLayout* layout);
FlLayoutAreaId fl_layout_add_area(struct FlContext* ctx, struct FlLayoutArea* area);

void fl_layout_use_area(struct FlContext* ctx, FlLayoutAreaId handle);


